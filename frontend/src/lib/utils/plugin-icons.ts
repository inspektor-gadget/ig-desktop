/**
 * Ensure an SVG has width and height attributes for consistent sizing.
 */
function normalizeSvgSize(svg: string, size: number = 16): string {
	// Skip if already has width/height
	if (/\bwidth\s*=/.test(svg) && /\bheight\s*=/.test(svg)) {
		return svg;
	}

	// Add width/height after the opening <svg tag
	return svg.replace(/^(<svg\s)/, `$1width="${size}" height="${size}" `);
}

/**
 * Resolve an icon value to SVG markup.
 * If the value starts with '<', it's assumed to be SVG and normalized.
 * Otherwise, it's looked up in the predefined icon set.
 */
export function resolvePluginIcon(icon: string | undefined): string {
	if (!icon) return '';

	if (icon.trim().startsWith('<')) {
		return normalizeSvgSize(icon);
	}

	return PLUGIN_ICONS[icon.toLowerCase()] || '';
}
